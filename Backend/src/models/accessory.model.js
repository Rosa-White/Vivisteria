import { Schema, model } from "mongoose";

const accessorySchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

const accessoryModel = model("Accessory", accessorySchema);
export default accessoryModel;